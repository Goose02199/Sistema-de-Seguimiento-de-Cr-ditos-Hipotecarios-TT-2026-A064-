def _mensualidad_total(credito, tasa_anual, plazo, adders, pol, subcuenta=0.0, tipo_producto="tradicional"):
    """
    Mensualidad total = (cap+int) + adders, respetando apertura financiada y
    abatimiento de subcuenta SOLO en 'apoyo'. Redondeo a peso.
    """
    apertura_pct = float(pol["comision_apertura_pct"])
    apertura_fin = bool(pol.get("comision_apertura_financiada", True))
    com_ap = credito * apertura_pct
    principal = credito + (com_ap if apertura_fin else 0.0)

    if tipo_producto == "apoyo":
        aplicar = min(subcuenta, principal)
        principal = max(0.0, principal - aplicar)

    tasa_m = float(tasa_anual) / 12.0
    f = factor_anualidad(tasa_m, plazo)
    cap_int = principal * f
    m_total = cap_int + adders
    return float(np.floor(m_total + 0.5))  # redondeo a peso

def _credito_tope_estructura(valor, enganche, credito_inst, subcuenta, pol, tipo_producto):
    """
    Tope de crédito Banorte por estructura y políticas (aforo Banorte, aforo total, etc.).
    """
    aforo_max_banorte = float(pol.get("aforo_max", 0.95))
    tope_total = pol.get("apoyos", {}).get("aforo_total_max_instituto", None)
    instituto_tiene_credito = bool(pol.get("apoyos", {}).get("instituto_tiene_credito", False)) or (credito_inst > 0)

    # Estructura según tipo
    if tipo_producto == "cofinavit":
        estruct = max(0.0, valor - (enganche + credito_inst + subcuenta))
    elif tipo_producto in ("fpt", "cta_infonavit"):
        estruct = max(0.0, valor - (enganche + credito_inst))
    else:  # tradicional / apoyo
        estruct = max(0.0, valor - enganche)

    credito_top = min(estruct, valor * aforo_max_banorte)

    if tope_total is not None and instituto_tiene_credito:
        # Límite por aforo total (instituto + Banorte)
        credito_top = min(credito_top, max(0.0, valor * float(tope_total) - credito_inst))

    return round(max(0.0, credito_top), 2)

