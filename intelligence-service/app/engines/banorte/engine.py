def productos_para_usuario(usuario: dict, productos_disponibles_db: list) -> list:
    """
    Filtra los productos que el banco ofrece según el perfil del usuario.
    Recibe:
    - usuario: El diccionario con los datos del cliente.
    - productos_disponibles_db: La lista de diccionarios (politicas_json) extraídos de la BD.
    """
    tiene_inf = bool(usuario.get("tiene_infonavit"))
    tiene_fov = bool(usuario.get("tiene_fovissste"))

    lista_productos = []

    for prod in productos_disponibles_db:
        tipo = prod.get("politicas", {}).get("tipo", "")
        
        # Regla 1: Todos pueden acceder al tradicional
        if tipo == "tradicional":
            lista_productos.append(prod)
            
        # Regla 2: Productos INFONAVIT
        elif tipo in ["apoyo", "cofinavit", "cta_infonavit"] and tiene_inf:
            lista_productos.append(prod)
            
        # Regla 3: Productos FOVISSSTE
        elif tipo == "fpt" and tiene_fov:
            lista_productos.append(prod)

    return lista_productos


