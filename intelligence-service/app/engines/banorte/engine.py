def rescate_por_enganche(
    valor, enganche_inicial, ingreso_usuario, plazo, pol, tipo_producto,
    credito_inst, subcuenta, tasas_df,
    adders,
    enganche_min_pct, enganche_max_pct=0.30, delta_enganche_pct=0.02,
    tol_mxn=50.0
):
    """
    Aumenta el enganche (>= mínimo del producto) en pasos delta_enganche_pct
    hasta lograr mensualidad <= ingreso/relacion_ir para alguna tasa.

    Parámetros clave:
    - valor: valor de la vivienda
    - enganche_inicial: enganche calculado con el % original del usuario
    - ingreso_usuario: ingreso bruto mensual
    - plazo: plazo en meses
    - pol: diccionario de políticas del producto
    - tipo_producto: 'tradicional', 'apoyo', 'cofinavit', 'cta_infonavit', 'fpt'
    - credito_inst: crédito del instituto (INFONAVIT / FOVISSSTE) si aplica
    - subcuenta: subcuenta de vivienda si aplica
    - tasas_df: DataFrame con columnas:
        - 'nombre_tasa'
        - 'tasa_anual_pct' (en porcentaje, ej. 9.78, no 0.0978)
    - adders: seg_vida + seg_danos + com_admin
    - enganche_min_pct: mínimo % de enganche permitido por el producto (ej. 0.10)
    - enganche_max_pct: máximo % de enganche a considerar en rescate (ej. 0.30)

    Devuelve:
    - (mejor_fila, df_ranking) si encontró algo
    - None si no hay forma de cumplir la capacidad de pago ni aumentando enganche
    """
    # Sanidad básica
    if valor <= 0:
        return None

    relacion_ir = float(pol["relacion_ir"])
    mensualidad_tope = float(ingreso_usuario) / relacion_ir

    # Punto de partida (respetar mínimo de producto)
    pct_usuario = enganche_inicial / valor  # puede venir por debajo del mínimo
    pct_actual = max(pct_usuario, enganche_min_pct)
    pct_actual = min(pct_actual, enganche_max_pct)

    mejores = []
    for _, r in tasas_df.iterrows():
        tasa_nombre = r["nombre_tasa"]
        tasa_anual = r["tasa_anual_pct"] / 100.0

        # Busca el menor % de enganche que cumpla para esta tasa
        pct = pct_actual
        cumple = None
        while pct <= enganche_max_pct + 1e-9:
            enganche = round(valor * pct, 2)
            credito_top = _credito_tope_estructura(
                valor, enganche, credito_inst, subcuenta, pol, tipo_producto
            )
            if credito_top <= 0:
                pct += delta_enganche_pct
                continue

            m = _mensualidad_total(
                credito_top, tasa_anual, plazo, adders, pol,
                subcuenta=subcuenta, tipo_producto=tipo_producto
            )
            if m <= mensualidad_tope + tol_mxn:
                cumple = (pct, enganche, credito_top, m)
                break
            pct += delta_enganche_pct

        if cumple is not None:
            pct_ok, eng_ok, cred_ok, m_ok = cumple
            mejores.append({
                "tasa_nombre": tasa_nombre,
                "tasa_anual_pct": r["tasa_anual_pct"],
                "pct_enganche_nuevo": pct_ok,
                "enganche_nuevo": eng_ok,
                "credito_max": cred_ok,
                "mens_compatible": m_ok
            })

    if not mejores:
        return None

    df = pd.DataFrame(mejores).sort_values(
        by=["mens_compatible", "tasa_anual_pct", "credito_max"],
        ascending=[True, True, False]
    ).reset_index(drop=True)

    return df.iloc[0], df

